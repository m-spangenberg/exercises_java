package exercise_01;

public class Variables
{
    public static void main(String[] args) {
        // In Java a variable must be declared with a datatype
        // Java is strongly typed, just like C

        // Integer
        int number1 = 500;
        System.out.println(number1);

        // Float
        double number2 = 10.0;
        System.out.println(number2);

        // Boolean
        boolean value1 = true;
        System.out.println(value1);

        // Chars
        char ch = 'A';
        System.out.println(ch);

        // Strings
        String a_string = "A collection of characters";
        System.out.println(a_string);

        // You can pass variables to other variables
        int ch_2 = ch;
    }
}
